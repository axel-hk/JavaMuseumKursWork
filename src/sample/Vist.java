package sample;

public class Vist {
    private String Name;
    private String Start;
    private String End;


    public Vist(String name, String start, String end) {
        Name = name;
        Start = start;
        End= end;

    }
    public Vist() {}
    public String getName() {
        return Name;
    }

    public String getStart() {
        return Start;
    }

    public String getEnd() {
        return End;
    }


}